export function MobileBottomNav() {
  const pathname = usePathname();

  return (
    <nav
      className="fixed bottom-0 left-0 right-0 z-50 flex border-t border-slate-200 bg-white sm:hidden"
      style={{ paddingBottom: "env(safe-area-inset-bottom, 0px)" }}
    >
      {NAV_ITEMS.map(({ href, label, icon: Icon, exact }) => {
        const isActive = exact ? pathname === href : pathname.startsWith(href);
        return (
          <Link
            key={href}
            href={href}
            className={`flex flex-1 flex-col items-center gap-0.5 py-2.5 text-[11px] font-medium transition-colors active:opacity-70 ${
              isActive ? "text-blue-600" : "text-slate-400"
            }`}
          >
            <Icon className={`h-5 w-5 transition-transform ${isActive ? "scale-110" : ""}`} />
            {label}
          </Link>
        );
      })}
    </nav>
  );
}

